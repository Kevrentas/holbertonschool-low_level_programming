#include "3-calc.h"

/* op_add - suma
 * @a: num
 * @b: num
 * Return : total
 * */

int op_add(int a, int b)
{
	return (a + b);
}
/* op_sub - resta
 * @a: num
 * @b: num
 * Return : total
 * */
int op_sub(int a, int b)
{
	return (a - b);
}
/* op_mul - multiplicaction
 * @a: num
 * @b: num
 * Return : total
 * */
int op_mul(int a, int b)
{
	return (a * b);
}
/* op_div - division
 * @a: num
 * @b: num
 * Return : total
 * */
int op_div(int a, int b)
{
	return (a / b);
}
/* op_mod - remainder of the division
 * @a: num
 * @b: num
 * Return : total
 * */
int op_mod(int a, int b)
{
	return (a % b);
}
